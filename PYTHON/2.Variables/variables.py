# Python is a "type-inferred" language, so you don't have to explicitly define the variable type
# Type inference refers to the automatic detection of the data type

# Declaring and assigning value to a variable
website = "apple.com"
# print(website)

# Assigning a new value of the variable "website"
website = "powercoders.org"

# Assigning the same value to multiple variables
x = y = z = "same"


# Assigning multiple values to multiple variables
a, b, c = 5, 3.2, "Hello"


